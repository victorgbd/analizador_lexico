    private void analizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_analizaActionPerformed
        File archivo = new File("archivo.txt");
        PrintWriter escribe;
        try{
            escribe = new PrintWriter(archivo);
            escribe.print(Texto.getText());
            escribe.close();
        }catch(FileNotFoundException e){
        }
        try {
            Reader leer  = new BufferedReader(new FileReader("archivo.txt"));
            Lexer lexer = new Lexer(leer);
            String resultado = "";
            while (true) {                
                Tokens tokens = lexer.yylex();
                if(tokens == null){
                    resultado += "fin";
                    txt_resultado.setText(resultado);
                }
                switch (tokens) {
                    case ERROR:
                        resultado += "Simbolo no definido\n";
                        break;
                    case Identificador: case Numero: case Reservadas:
                        resultado += lexer.lexeme + " Token: " + tokens + "\n";
                        break;
                    default:
                        resultado += " Token: " + tokens + "\n";

                        break;
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(analizador_Form.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(analizador_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_analizaActionPerformed

